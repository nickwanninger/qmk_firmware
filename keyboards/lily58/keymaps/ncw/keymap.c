bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    static bool emod_used = false;

    if (record->event.pressed && layer_state_is(_EMOD)) {
        emod_used = true;
    }
    switch (keycode) {
        case KC_EMOD:
            if (record->event.pressed) {
                emod_used = false;
                layer_on(_EMOD);
            } else {
                layer_off(_EMOD);
                if (!emod_used) tap_code(KC_ESC);
            }
            break;
    }
    return true;
}

